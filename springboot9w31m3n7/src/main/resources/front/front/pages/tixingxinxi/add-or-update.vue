			</view>
         
			<view :style='{"padding":"12rpx 0","margin":"0 0 24rpx 0","borderColor":"#ECE0FF","alignItems":"center","borderWidth":"0","display":"flex","width":"100%","borderStyle":"solid","height":"auto"}' class="">
				<view :style='{"width":"160rpx","padding":"0 20rpx 0 0","lineHeight":"80rpx","fontSize":"28rpx","color":"#727272","textAlign":"right"}' class="title">提醒内容</view>
				<textarea :style='{"border":"0","padding":"24rpx","margin":"0px","color":"#000","borderRadius":"8rpx","flex":"1","background":"#FCF3EF","fontSize":"28rpx","height":"240rpx"}' v-model="ruleForm.tixingneirong" placeholder="提醒内容" :maxlength="-1"></textarea>
